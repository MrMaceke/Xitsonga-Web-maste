                    <h3>RIDE SAFETY</h3>

                    <p>Kutoa brings people together to share rides. The community is made possible by friends, family, colleagues and people meeting for the first time, who are willing to commute together.</p>
                    <p>We understand it may be exciting to meet someone for the first time. However your safety is very important, and because you are in control of your Kutoa experience, there are certain safety steps that you should follow before and during Kutoa rides.</p>
                    <p>We ask you to read the tips and information below, and strongly urge you to follow these guidelines in the interest of your personal safety and well-being. However, you are always the best judge of your own safety, and these guidelines are not intended to be a substitute for your own judgment</p>
                    <h3 style="color:#FF7F00">Before trip behavior</h3>
                    <p><b>Commute With Mutual Friends</b> - Check user facebook profile using the verify link, we encourage you to share rides with people you at least a mutual friend with on Facebook.</p>
                    <p><b>Cancel Suspicious Trip</b> - We encourage you to cancel a trip if you feel uncomfortable for whatever reason.</p>
                    <h3 style="color:#FF7F00">During trip behavior</h3>
                    <p><b>Tell Friends and Family Your Plans</b> - Inform a friend or family member of your plans and when and where you're going. Make sure you have your cell phone charged and with you at all times.</p>
                    <p><b>Cancel Suspicious Driver Or Passenger Trip</b> - On the day of the trip, when you meet the driver or passenger, if you feel uncomfortable for whatever reason, don't take the trip.</p>
                    <p><b>Keep Your Phone and Location ON</b> - Keep your phone and location ON during the trip. Kutoa will be able to track you, this information is available to your facebook friends on request.</p>
                    <p><b>Rate Passenger or Driver After Trip</b> - Tell us your experience with the driver or passenger during the trip.</p>
